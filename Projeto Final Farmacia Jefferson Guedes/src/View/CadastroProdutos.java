    private void btSalvarpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarpActionPerformed
        String item, unidadedemedida;
        int estoqueminimo;
        int codigoproduto;
        double custounitario;
        double precounitario;

        item = txtItem.getText();
        codigoproduto = Integer.parseInt(txtCodigodoproduto.getText());
        custounitario = Double.parseDouble(txtCustounitario.getText());
        precounitario = Double.parseDouble(txtPrecounitario.getText());
        unidadedemedida = txtUnidadedemedida.getText();
        estoqueminimo = Integer.parseInt(txtEstoqueminimo.getText());

        if (codigoproduto == 0) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Código do Produto obrigatório!");
        }
        if (item.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Item obrigatório!");
        }
        if (custounitario == 0) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Custo Unitário obrigatório!");
        }
        if (precounitario == 0) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Preço Unitário obrigatório!");
        } else if (unidadedemedida.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Unidade de Medida obrigatório!");
        } else {
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
        }

        CadastroProdutosDTO cpdto = new CadastroProdutosDTO();
        cpdto.setItem(item);
        cpdto.setCodigoProduto(codigoproduto);
        cpdto.setCustoUnitario(custounitario);
        cpdto.setPrecoUnitario(precounitario);
        cpdto.setUnidadeDeMedida(unidadedemedida);
        cpdto.setEstoqueMinimo(estoqueminimo);

        CadastroProdutosDAO cpdao = new CadastroProdutosDAO();
        cpdao.cadastroProdutodao(cpdto);


    }//GEN-LAST:event_btSalvarpActionPerformed

