    private void listarValoresProdutos() {
        try {
            CadastroProdutosDAO objCadastroprodutosDAO = new CadastroProdutosDAO();

            DefaultTableModel model = (DefaultTableModel) tabelaProdutos.getModel();
            model.setNumRows(0);

            ArrayList<CadastroProdutosDTO> lista = objCadastroprodutosDAO.pesquisarCadastroProdutos();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getItem(),
                    lista.get(num).getCodigoProduto(),
                    lista.get(num).getUnidadeDeMedida(),
                    lista.get(num).getEstoqueMinimo(),
                    lista.get(num).getCustoUnitario(),
                    lista.get(num).getPrecoUnitario()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Listar Valores tela Produto" + e.getMessage());
        }

    }
    
