    private void btSalvarpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarpActionPerformed
        String datavenda, nomedoproduto, loja;
        int quantidadevendida, codigoproduto, codigoloja, codigosaida;
        double valorvendatotal, precounitario;

        //codigosaida = Integer.parseInt(txtCodigosaida.getText()); 
        datavenda = ftDatadavenda.getText();
        nomedoproduto = txtNomedoprodutos.getText();
        loja = txtLoja.getText();
        quantidadevendida = Integer.parseInt(txtQuantidade.getText());
        precounitario = Double.parseDouble(txtPrecounitario.getText());
        valorvendatotal = Double.parseDouble(txtValordevendatotal.getText());
        codigoproduto = Integer.parseInt(txtCodigodoprodutos.getText());
        codigoloja = Integer.parseInt(txtCodigodaloja.getText());
      

        ControleSaidaDTO objcadastrosaidadto = new ControleSaidaDTO();
        objcadastrosaidadto.setDataVenda(datavenda); 
        objcadastrosaidadto.setNomeDoProduto(nomedoproduto);
        objcadastrosaidadto.setLoja(loja);
        objcadastrosaidadto.setQuantidadeVendida(quantidadevendida);
        objcadastrosaidadto.setValorVendaTotal(valorvendatotal);
        objcadastrosaidadto.setPrecoUnitario(precounitario);
        objcadastrosaidadto.setCodigoProduto(codigoproduto);
        objcadastrosaidadto.setCodigoLoja(codigoloja);
        //objcadastrosaidadto.setCodigoSaida(codigosaida);

        if (codigoproduto == 0) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Codigo Produto obrigatório!");
        }
        if (codigoloja == 0) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Codigo Loja obrigatório!");
        }
        if (quantidadevendida == 0) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Quantidade Vendida obrigatório!");
        }
        if (loja.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Loja obrigatório!");
        }
        if (nomedoproduto.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Nome do Produto obrigatório!");
        }
        if (datavenda.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preenchimento da Data de Venda obrigatório!");
        }
        if (valorvendatotal == 0) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Total Vendido obrigatório!");
        } else if (precounitario == 0) {
            JOptionPane.showMessageDialog(null, "Preenchimento do Preço Unitário obrigatório!");
        } else {
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
        }

        ControleSaidaDAO objcontrolesaidadao = new ControleSaidaDAO();
        objcontrolesaidadao.cadastroSaidadao(objcadastrosaidadto);
    }//GEN-LAST:event_btSalvarpActionPerformed

