    private void listarValoresSaida() {
        try {
            ControleSaidaDAO objControleSaidaDAO = new ControleSaidaDAO();

            DefaultTableModel model = (DefaultTableModel) tabelaSaida.getModel();
            model.setNumRows(0);

            ArrayList<ControleSaidaDTO> lista = objControleSaidaDAO.pesquisarControleSaida();

            for (int num = 0; num < lista.size(); num++) {
                model.addRow(new Object[]{
                    lista.get(num).getCodigoSaida(),
                    lista.get(num).getDataVenda(),
                    lista.get(num).getCodigoProduto(),
                    lista.get(num).getNomeDoProduto(),
                    lista.get(num).getCodigoLoja(),
                    lista.get(num).getLoja(),
                    lista.get(num).getQuantidadeVendida(),
                    lista.get(num).getPrecoUnitario(),
                    lista.get(num).getValorVendaTotal()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Listar Valores tela Saida" + e.getMessage());
        }
    }
