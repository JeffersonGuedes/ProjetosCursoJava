
public class Funcionarios extends Clientes {

    private String codigoFuncionarios;
    private String cargo;
    private double salario;

    public String getCodigoFuncionarios() {
        return codigoFuncionarios;
    }

    public void setCodigoFuncionarios(String codigoFuncionarios) {
        this.codigoFuncionarios = codigoFuncionarios;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public double getSalario() {
        return salario;
    }

    public void setSalario(double salario) {
        this.salario = salario;
    }

}
