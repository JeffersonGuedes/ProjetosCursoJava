
public class Vendas extends Produtos{
    private String codigoVenda;
    private String codigoFuncionarios;
    private String codigoClientes;
    private String codigoProdutos;
    private double Venda;

    public String getCodigoProdutos() {
        return codigoProdutos;
    }

    public void setCodigoProdutos(String codigoProdutos) {
        this.codigoProdutos = codigoProdutos;
    }

    public String getCodigoVenda() {
        return codigoVenda;
    }

    public void setCodigoVenda(String codigoVenda) {
        this.codigoVenda = codigoVenda;
    }

    public String getCodigoFuncionarios() {
        return codigoFuncionarios;
    }

    public void setCodigoFuncionarios(String codigoFuncionarios) {
        this.codigoFuncionarios = codigoFuncionarios;
    }

    public String getCodigoClientes() {
        return codigoClientes;
    }

    public void setCodigoClientes(String codigoClientes) {
        this.codigoClientes = codigoClientes;
    }

    public double getVenda() {
        return Venda;
    }

    public void setVenda(double Venda) {
        this.Venda = Venda;
    }

    
    
    
}
